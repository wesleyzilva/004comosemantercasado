/*
 * Herança: permite basear uma nova classe na definição de uma outra classe
 * previamente existente. Será aplicada tanto para as características quanto
 * para os comportamentos.
 * 
 * 
 * Raiz: uma classe que não possui superclasses (mãe). Folha: uma classe que não
 * possui subclasses (filhas). Descendente: A teve um filho AC e AC teve um
 * filho ACA. A é ancestral de ACA. A NÃO É ANCESTRAL DE AC Ascendente: ACA é
 * descendente de A. AC NÃO É DESCENDENTE DE A Especialização: percorrer a
 * árvore de cima pra baixo. Generalização: percorrer a árvore de baixo pra
 * cima.
 * 
 * 
 * HERANÇA DE IMPLEMENTAÇÃO (HERANÇA POBRE): Apenas herda os atributos e os
 * métodos da classe mãe, sem implementar mais nenhum.
 * 
 * 
 * HERANÇA PARA DIFERENÇA: Herda os atributos e os métodos da classe mãe,
 * implementando outros métodos dela própria.
 * 
 * 
 * Classe abstrata: não pode ser instanciada. Só pode servir como progenitora.
 * (Não pode gerar objetos) Método abstrato: declarado, mas não implementado na
 * progenitora. (Interfaces) Classe final: não pode ser herdada por outra
 * classe. É obrigatoriamente folha. (Não pode ter filhos) Método final: não
 * pode ser sobrescrito pelas suas sub-classes. É obrigatoriamente herdado. (Não
 * pode gerar uma especialização)
 * 
 *
 * Sobrepor: fazer a mesma coisa de formas diferentes (@Override).
 */